package com.spring.pointcut;

public class BehaviorImpl implements Behavior{

	@Override
	public void 잠자기() {
		System.out.println("쌔근쌔근 코");
	}

	@Override
	public void 공부하기() {
		System.out.println("공부하는게 제일 조아");
	}

	@Override
	public void 밥먹기() {
		System.out.println("냠냠");
	}

	@Override
	public void 데이트() {
		System.out.println("라브라브");
	}

	@Override
	public void 운동() {
		System.out.println("허슬허슬");
	}

	@Override
	public void 놀기() {
		System.out.println("노는게 제일 조아");
	}

	@Override
	public void 정신수양() {
		System.out.println("아수라 발발타");
	}

	
}
